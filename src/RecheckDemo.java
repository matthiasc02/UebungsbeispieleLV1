
public class RecheckDemo {
	public static void main(String[] args) {
		int breite = 2;
		int laenge = 3;
		int umfang = 0;
		int flaeche = 0;
	    
		umfang=((2*breite)+(2*laenge));
		flaeche=(breite*laenge);
		
		System.out.println("Der Umfang beträgt: "+umfang);
		System.out.println("Die Fläche beträgt: "+flaeche);
	}

}
